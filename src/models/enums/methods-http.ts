/**
 * Represents all the type of actions of methods HTTP.
 */
export enum MethodsHttp {
	/** POST action type. */
	POST = 'post',

	/** GET action type. */
	GET = 'get',

	/** Delete action type. */
	DELETE = 'delete',

	/** Put action type. */
	PUT = 'put',

	/** Patch action type. */
	PATCH = 'patch',
}
